package calculator;
public class calculation {
    public int adder(int a,int b){
        return a+b;
    }
    public int subtract(int a,int b){
        return a-b;

    }
    public int division(int a,int b){
        return a/b;

    }
    public int multiplication(int a,int b){
        return a*b;
    }


}
